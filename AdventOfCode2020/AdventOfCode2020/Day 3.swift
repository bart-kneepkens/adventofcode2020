fileprivate let inputRowArrays = input.components(separatedBy: .newlines).map({ Array($0) })

struct Day3 {
    struct Slope {
        var x: Int
        var y: Int
    }
    
    static func solve(slope: Slope = Slope(x: 3, y: -1)) -> Int {
        var x: Int = 0
        var y: Int = 0
        
        let horizontalPatternLength = inputRowArrays.first!.count
        let goal = -inputRowArrays.count
        
        var counter = 0
        
        while y + slope.y > goal {
            x += slope.x
            y += slope.y
            
            let correctedX = x < horizontalPatternLength ? x : x % horizontalPatternLength
            let correctedY = abs(y)
            
            if inputRowArrays[correctedY][correctedX] == "#" {
                counter += 1
            }
        }
        
        return counter
    }
    
    static func solve2() -> Int {
        [Slope(x: 1, y: -1),
         Slope(x: 3, y: -1),
         Slope(x: 5, y: -1),
         Slope(x: 7, y: -1),
         Slope(x: 1, y: -2),
            ]
            .map({ Day3.solve(slope: $0) }).reduce(1, *)
    }
}
